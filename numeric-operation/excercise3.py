first_value = 5
second_value = 4

sum = first_value + second_value
difference = first_value - second_value
product = first_value * second_value
quotient = first_value / second_value
modulus = first_value % second_value
exponent = first_value ** second_value 

print('Sum: \t\t' + str(sum))
print('Difference:\t ' + str(difference))
print('Product: \t' + str(product))
print('Quotient: \t' + str(quotient))
print('Modulus: \t' + str(modulus))
print('Exponent: \t' + str(exponent))